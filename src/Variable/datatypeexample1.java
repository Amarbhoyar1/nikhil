package Variable;

public class datatypeexample1 
{
	public static void main(String[] args) 
	{
		int a;
		a=100;
		System.out.println(a);
		
		
		float d;
		d=54.34f;
		System.out.println(d);
		
		
		char e;
		e='A';
		System.out.println(e);
		
		
		String Address;
		Address="Arvi Naka Wardha";
		System.out.println(Address);
		
}

}
